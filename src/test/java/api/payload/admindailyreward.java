package api.payload;

public class admindailyreward {
	
	
	String day;
	String rewardType;
	String rewardShortCode;
	String quantity;
	
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getRewardType() {
		return rewardType;
	}
	public void setRewardType(String rewardType) {
		this.rewardType = rewardType;
	}
	public String getRewardShortCode() {
		return rewardShortCode;
	}
	public void setRewardShortCode(String rewardShortCode) {
		this.rewardShortCode = rewardShortCode;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	
	
	

}
